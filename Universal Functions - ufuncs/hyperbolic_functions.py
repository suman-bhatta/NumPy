"Hyperbolic Functions"

import numpy as np

x = np.sinh(np.pi/2)

print(x)


"Find hyperbolic sine values for all of the values in arr:"

arr = np.array([np.pi/2, np.pi/3, np.pi/4, np.pi/5])

x = np.sinh(arr)

print(x)


"Finding Angles"

x = np.arcsinh(1.0)

print(x)


"Angles of Each Value in Arrays"

